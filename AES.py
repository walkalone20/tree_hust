    Key_Array = Trans_Array(IntKey)
    CalculateExtendKeyArray(Key_Array)
    for i in range(0, len(Plaintext), 16):
        Text = []
        for j in range(i, i + 16):
            Text.append(ord(Plaintext[j]))
        temp = Encode(Trans_Array(Text))
        for j in Trans_List(temp):
            ans.append(j)
    return ans, oldlen


